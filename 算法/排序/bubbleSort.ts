/**
 * 冒泡排序
 */
export function bubbleSort(arr: number[]): number[] {
  const array = Array.from(arr);
  for (let i = 0; i < array.length - 1; i++) {
    for (let j = 0; j < array.length - i - 1; j++) {
      if (array[j] > array[j + 1]) {
        [array[j], array[j + 1]] = [array[j + 1], array[j]];
      }
    }
  }
  return array;
}
